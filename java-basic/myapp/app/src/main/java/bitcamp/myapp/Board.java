package bitcamp.myapp;

public class Board {

  String title;
  String content;
  String writer;
  String createdDate;

  public Board() {

  }

  public Board(String title, String content, String writer, String createdDate) {
    this.title = title;
    this.content = content;
    this.writer = writer;
    this.createdDate = createdDate;
  }

}
